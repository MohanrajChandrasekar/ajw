'use strict'

var Sequelize = require('../../../config/sequelize').getConnection();

exports.indexDocument = function (req, res) {
    console.log(req.body);

    Sequelize
        .query('EXEC Master_GetDocumentDetails')
        .then(queryReturn);

    function queryReturn(recs) {
        // console.log(recs[0]);
        res.send(recs[0]);
    };

};

exports.indexById = function(req, res) {
    console.log(req.body);
    
    var replacements1 = {
        id: req.params.id
    };
    Sequelize
        .query('EXEC Master_GetDocumentDetailById :id',
            {replacements : replacements1})
        .then(queryReturn);

    function queryReturn(recs) {
        res.send(recs[0][0]);
    };

};
